package Assignment2;

public class table2 {
    public static void main(String[] args) {
        int table=2;
        int count=1;
        while(count<=10){
            System.out.println(table*count);
            count++;
        }
        System.out.println();
        for(int i=1;i<=10;i++){
             System.out.println(table*i);
        }
    }
    
}
